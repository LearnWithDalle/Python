# Comparison (Relational) Oprators
'''
1. == : equl to 
2. != : not equl to
3. <  : Less than 
4. >  : Greater than
5. <= : Less than or equl to
6. >= : Greater than or equl to
7. <> : x <> y if not equl then true (!=)
'''

x = 4 
y = 2
print("x: " , x)
print("y: " , y)
print("x == y", x == y) # false
print("x != y", x != y) # true
print("x < y", x < y) # false
print("x > y", x > y) # true
print("x <= y", x <= y) # false
print("x >= y", x >= y) # true
