# Arithmetic Oprators

'''
Oprators in Arithmetic Oprators:
1. + : x + y
2. - : x - y
3. * : x * y  
4. / : x / y  
5. % : x % y
6. **: x ** y
7. // : x // y
'''

x = 15
y = 4

# addition
print(x , "+", y, "=",  x + y )
# Subtration
print(x , "-", y, "=",  x - y )
# Multiplication
print(x , "*", y, "=",  x * y )
# Division
print(x , "/", y, "=",  x / y )
# Module
print(x , "%", y, "=",  x % y )
# explonent
print(x , "**", y, "=",  x ** y )
# floore Division
print(x , "//", y, "=",  x // y )
