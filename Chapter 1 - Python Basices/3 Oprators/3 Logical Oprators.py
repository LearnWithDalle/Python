'''
Logical Oprators 
1. and : true if both are true
2. or  : true if atleast one are true
3. not : true if the ans is false
'''

# ex.
x = 15
y = 10
j = 20

print("this is and : ", x > y and x > j)
print("this is or : ", x > j or x > y)
print("this is not for false: ", not(x > j)) # false 
print("this is not for true: ", not(x > y)) # true
